let grade = 'C';

switch (grade) {
    case 'A+': console.log("Outstanding");
        break;
    case 'A': console.log("Excellent");
        break;
    case 'B': console.log("Very Good");
        break;
    case 'C': console.log("Good");
        break;
    case 'D': console.log("Average");
        break;
    case 'E': console.log("Need HardWork");
        break;
    default: console.log("Wrong Input");
}